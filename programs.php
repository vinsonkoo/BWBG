		<h1> SIGNATURE PROGRAMS</h1>

		<p>Members of the Black Women for Black Girls Giving Circle plan and execute the organization's Civic Leadership &amp; College Readiness Program. The CLCRP is an enrichment program offered through partnership with BWBG and a designated youth development organization each year. The CLCRP is funded by BWBG and is open to young women that self-identify as Black and that are entering into 12th grade of high school and are currently enrolled with our partner organizaion.</p>

		<p>There are four components to the CLCRP (1) BWBG Civic Leadership Fellowship, (2) BWBG Enrichment Workshops, (3) BWBG Mentorship Program, and (4) The College Shower. Each component is described below.</p>

		<ul class="programs">
			<li>
				<h3>a. BWBG Civic Leadership Fellowship</h3>
				<p>As a giving circle one of our primary goals is to calls is to support young Black girls as they try to attain life success in a difficult urban environment. The BWBGgc Civic Leadership Fellowship will create a pipeline of young diverse Black women leaders. Participants will construct a tangible community service project based on key findings from the research commissioned by BWBGgc. Fellows will work towards the achievement of their full stipend at the completion of the fellowship cycle.</p>
			</li>
			<li>
				<h3>b. BWBG Enrichment Workshops</h3>
				<p>As a complement to the BWBG Civic Leadership Fellowship and Mentoring Programs, we offer year-round enrichment workshops. These workshops enhance the learning environment for program participants by giving them a chance to apply their skills, imagination and critical thinking abilities, to some fun and real-life experiences. All workshops are developed and facilitated by BWBGgc members and specailly invited experts.</p>
			</li>
			<li>
				<h3>c. Mentorship</h3>
				<p>Civic Leadership Fellows in the 12th grade are connected with a virtual mentoring circle. Throughout the year mentors and mentees have opportunities to exchange and engage at BWBG affiliated events and at enrichment workshops. This circle follows through the girls' freshman year of college. The goal is to establish a network of support for each young lady that will not fluctuate with changes in the lives of BWBG members and that draws upon the unique backgrounds of all members.</p>
			</li>
			<li>
				<h3>d. College Shower</h3>
				<p>After successfully completing the BWBG Civic Leadership &amp; College Readiness Program; each Fellow who has met her high school academic requirements, is a graduating senior. And has been accepted into a two or four year institution of higher education, will participate in the BWBG College Shower. This signature event will be the climax of the program and will serve as the recognition and awards ceremony, applauding the achievements of each young woman, championing the next chapter in her life, and encouraging her pursuit of a college degree.
 
				<a href="main.php?page=getinvolved">Find out how you can get involved in the BWBG Civic Leadership &amp; College Readiness Program.</a></p>
			</li>


